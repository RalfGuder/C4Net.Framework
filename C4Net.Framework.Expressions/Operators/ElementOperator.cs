namespace C4Net.Framework.Expressions.Operators
{
    /// <summary>
    /// Operators between elements
    /// </summary>
    public enum ElementOperator
    {
        /// <summary>
        /// Equals operator
        /// </summary>
        Equal,

        /// <summary>
        /// Not equals operator
        /// </summary>
        NotEqual,

        /// <summary>
        /// Greater operator
        /// </summary>
        Greater,

        /// <summary>
        /// Greater or equal operator
        /// </summary>
        GreaterOrEqual,

        /// <summary>
        /// Less operator
        /// </summary>
        Less,

        /// <summary>
        /// Less or equal operator
        /// </summary>
        LessOrEqual,

        /// <summary>
        /// Is Null operator
        /// </summary>
        IsNull,

        /// <summary>
        /// Is not Null operator
        /// </summary>
        IsNotNull,

        /// <summary>
        /// Like operator
        /// </summary>
        Like,

        /// <summary>
        /// Not like operator
        /// </summary>
        NotLike,

        /// <summary>
        /// Betweeen operator
        /// </summary>
        Between,

        /// <summary>
        /// Not between operator
        /// </summary>
        NotBetween,

        /// <summary>
        /// In operator
        /// </summary>
        In,

        /// <summary>
        /// Not in operator
        /// </summary>
        NotIn
    }
}
